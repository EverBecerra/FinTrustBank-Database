CREATE DATABASE IF NOT EXISTS FinTrustBank;
USE FinTrustBank;

-- Schema
CREATE TABLE IF NOT EXISTS Branches (
	Branch_ID VARCHAR(10) PRIMARY KEY,
	Branch_Name VARCHAR(100) NOT NULL DEFAULT '',
	Address VARCHAR(500) NOT NULL DEFAULT '',
	Phone VARCHAR(20) UNIQUE NOT NULL
);
     
CREATE TABLE IF NOT EXISTS Employees (
	Employee_ID INT AUTO_INCREMENT PRIMARY KEY,
	First_Name VARCHAR(10) NOT NULL DEFAULT '',
	Last_Name VARCHAR(10) NOT NULL DEFAULT '',
	Job_Title VARCHAR(20) NOT NULL DEFAULT '',
	Branch_ID VARCHAR(10) NOT NULL,
	Email VARCHAR(50) UNIQUE NOT NULL,
	Phone VARCHAR(20) UNIQUE NOT NULL,
	Salary DECIMAL(10,2) NOT NULL,
	FOREIGN KEY (Branch_ID) REFERENCES Branches(Branch_ID) 
);
     
CREATE TABLE IF NOT EXISTS Branch_Holdings (
	Vault_ID INT PRIMARY KEY AUTO_INCREMENT, 
	Branch_ID VARCHAR(10),  
    Holdings_Amount DECIMAL(12,2) NOT NULL CHECK(Holdings_Amount >= 10000),
    FOREIGN KEY (Branch_ID) REFERENCES Branches(Branch_ID)
);
    
CREATE TABLE IF NOT EXISTS Customers (
	Cust_ID INT AUTO_INCREMENT PRIMARY KEY,
	Cust_first_name VARCHAR(100) NOT NULL DEFAULT '',
	Cust_last_name VARCHAR(100) NOT NULL DEFAULT '',
	Cust_email VARCHAR(100) UNIQUE NOT NULL,
	Cust_phone VARCHAR(20) UNIQUE NOT NULL,
	Cust_birth_date DATE NOT NULL,
	Cust_Checkings_type ENUM('Savings', 'Checking', 'Business', 'Fixed Deposit', 'Investment') NOT NULL DEFAULT 'Checking',
	Cust_ssn VARBINARY(255) NOT NULL, -- Encrypts the SSN
	Credit_score SMALLINT NOT NULL CHECK (Credit_score BETWEEN 300 AND 850),
	Cust_address VARCHAR(500) NOT NULL DEFAULT '',
	Cust_status ENUM('Active', 'Suspended', 'Closed') NOT NULL DEFAULT 'Active',
	Branch_ID VARCHAR(10) NOT NULL,
	FOREIGN KEY (Branch_ID) REFERENCES Branches(Branch_ID)
);
	
CREATE TABLE IF NOT EXISTS Checkings (
	Checkings_ID INT AUTO_INCREMENT PRIMARY KEY,
	Customer_ID INT NOT NULL,
	Checkings_Type ENUM('Savings', 'Checking', 'Business', 'Fixed Deposit', 'Investment') NOT NULL,
	Checkings_Balance DECIMAL(12,2) NOT NULL DEFAULT 0.00,
	Status ENUM('Active', 'Inactive', 'Closed', 'Account Frozen') NOT NULL DEFAULT 'Active',
	FOREIGN KEY(Customer_ID) REFERENCES Customers(Cust_ID)
);
     
CREATE TABLE IF NOT EXISTS ATM_Service (
	ATM_Service_ID INT AUTO_INCREMENT PRIMARY KEY,
    Branch_ID VARCHAR(10) NOT NULL,
    Service_Type ENUM('Withdrawal', 'Deposit', 'Balance Inquiry', 'Transfer') NOT NULL,
    Service_Date TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    Amount DECIMAL(12, 2) DEFAULT 0.00,
    FOREIGN KEY (Branch_ID) REFERENCES Branches(Branch_ID)
);

CREATE TABLE IF NOT EXISTS In_Branch_Transactions (
	Branch_Transcation_ID INT AUTO_INCREMENT PRIMARY KEY,
    Branch_ID VARCHAR(10) NOT NULL,
    Service_Type ENUM('Withdrawal', 'Deposit', 'Balance Inquiry', 'Transfer') NOT NULL,
    Service_Date TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    Amount DECIMAL(12, 2) DEFAULT 0.00,
    FOREIGN KEY (Branch_ID) REFERENCES Branches(Branch_ID)
);
    
create table Customer_Service_Tickets (
	Ticket_ID INT AUTO_INCREMENT PRIMARY KEY,
    Checkings_ID INT NOT NULL,
    Created_Date TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    Issue_Category VARCHAR(100) NOT NULL,     
    Description TEXT,
    Status ENUM('Open', 'In Progress', 'Resolved', 'Closed') DEFAULT 'Open',
    Resolution_Notes TEXT,
    FOREIGN KEY (Checkings_ID) REFERENCES Checkings(Checkings_ID)
);

CREATE TABLE Service_Feedback (
	Feedback_ID INT AUTO_INCREMENT PRIMARY KEY,
    Ticket_ID INT UNIQUE NOT NULL,
    Rating INT CHECK (Rating BETWEEN 1 AND 5),
    Comments VARCHAR(300),
    Submitted_At TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (Ticket_ID) REFERENCES Customer_Service_Tickets(Ticket_ID)
);

CREATE TABLE Loan_Applications (
	Application_ID INT AUTO_INCREMENT PRIMARY KEY,
    Customer_ID INT NOT NULL,
    Application_Date DATE NOT NULL,
    Loan_Type ENUM('Personal', 'Home', 'Auto', 'Education', 'Business') NOT NULL,
    Amount_Requested DECIMAL(12, 2) NOT NULL,
    FOREIGN KEY (Customer_ID) REFERENCES Customers(Cust_ID) 
);

CREATE TABLE IF NOT EXISTS Credit_Cards (
	Card_ID INT AUTO_INCREMENT PRIMARY KEY,
    Customer_ID INT NOT NULL,
    Card_Type ENUM('Visa', 'MasterCard', 'Amex', 'RuPay') NOT NULL,
    Card_Number VARCHAR(16) UNIQUE NOT NULL,
    Expiry_Date DATE NOT NULL,
    CVV INT NOT NULL,
    Credit_Limit DECIMAL(10,2) NOT NULL,
    Outstanding_Balance DECIMAL(10,2) DEFAULT 0.00,
    Status ENUM('Active', 'Blocked', 'Expired') DEFAULT 'Active',
    FOREIGN KEY (Customer_ID) REFERENCES Customers(Cust_ID) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS Online_Banking_Logins (
	Login_ID INT AUTO_INCREMENT PRIMARY KEY,
    Customer_ID INT NOT NULL,
    Login_Timestamp DATETIME DEFAULT CURRENT_TIMESTAMP,
    Login_Status ENUM('Success', 'Failed') NOT NULL,
    IP_Address VARCHAR(45) NOT NULL,
    FOREIGN KEY (Customer_ID) REFERENCES Customers(Cust_ID) 
);

CREATE TABLE IF NOT EXISTS Fixed_Deposits (
	FixedDeposits_ID INT AUTO_INCREMENT PRIMARY KEY,
    Customer_ID INT NOT NULL,
    Amount DECIMAL(15, 2) NOT NULL,
    Interest_Rate DECIMAL(5, 2) NOT NULL,
    Maturity_Date DATE NOT NULL,
    Status ENUM('Active', 'Closed') NOT NULL,
    FOREIGN KEY (Customer_ID) REFERENCES Customers(Cust_ID) 
);
    
CREATE TABLE IF NOT EXISTS FD_Interest_Schedule (
    Schedule_ID INT PRIMARY KEY AUTO_INCREMENT,
    FixedDeposits_ID INT,
    Interest_Amount DECIMAL(10,2),
    Scheduled_Date DATE,
    Status ENUM('Scheduled', 'Paid') DEFAULT 'Scheduled',
    FOREIGN KEY (FixedDeposits_ID) REFERENCES fixed_deposits(FixedDeposits_ID)
);

CREATE TABLE IF NOT EXISTS Checkings_Beneficiaries ( 
	Beneficiary_ID INT AUTO_INCREMENT PRIMARY KEY,
    Checkings_ID INT NOT NULL,
    Beneficiary_Name VARCHAR(255) NOT NULL,
    Beneficiary_Checkings_Number VARCHAR(255) NOT NULL,
    Added_At DATETIME,
    Status ENUM ('Active', 'Inactive'),
    FOREIGN KEY (Checkings_ID) REFERENCES Checkings(Checkings_ID)
);

